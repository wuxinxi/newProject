public class BillOpenHelper extends DaoMaster.OpenHelper {

    public BillOpenHelper(Context context, String name) {
        super(context, name);
    }

    @Override
    public void onUpgrade(Database db, int oldVersion, int newVersion) {
        super.onUpgrade(db, oldVersion, newVersion);
        switch (oldVersion) {
            case 1:
                SpendDao.createTable(db, true);
                // 加入新字段 score
//                db.execSQL("ALTER TABLE 'STUDENT' ADD 'SCORE' TEXT;");
                break;
        }
    }
}
